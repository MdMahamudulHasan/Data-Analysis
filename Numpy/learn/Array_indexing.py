
# Array indexing is the same as accessing an array element.

# You can access an array element by referring to its index number.


# -------------  Access 1D Arrays Element---------------------------

import numpy as np
arr = np.array([1, 2, 3, 4])
print(arr[0])
print(arr[2])
print(arr[1:2])
print(arr[1] + arr[2])

